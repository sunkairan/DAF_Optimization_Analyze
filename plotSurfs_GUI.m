function toggleLine(groupID, handles)
if(groupID>2)
    selected = get(eval(['handles.popupmenu' num2str(groupID)]),'Value');
    % 1: All; 2: 20; 3: 15; 4: 10; 5: 5; 6: 1
    if(selected==1)
        startN = 2;
        stepN = 1;
    else
        startN = selected;
        stepN = 5;
    end
else
    startN = 2;
    stepN = 1;
end
onoff = get(eval(['handles.checkbox' num2str(groupID)]),'Value');
global allLineHandles
for i=2:allLineHandles{groupID,1}+1 
    if(isvalid(allLineHandles{groupID,i}) )
        allLineHandles{groupID,i}.Visible='off'; 
    end
end
if (onoff)
    for i=startN:stepN:allLineHandles{groupID,1}+1 
        if(isvalid(allLineHandles{groupID,i}) )
            allLineHandles{groupID,i}.Visible='on'; 
        end
    end
end
